package Category;


public class Category {

	private int id;
	private String catid;
	private String catname;
	
	public Category(int id, String catid, String catname) {
		super();
		this.id = id;
		this.catid = catid;
		this.catname = catname;
	}

	public int getId() {
		return id;
	}

	public String getCatid() {
		return catid;
	}

	public String getCatname() {
		return catname;
	}

	
	
	

	
}
